replace_colors() {
  method=$1
  str=$2

  if [ "$method" == "r" ]; then
    str=${str//"!"/""}
    str=${str//">"/""}
    str=${str//"+"/""}
  elif [ "$method" == "a" ]; then
    str=${str//" "/"\033[0m "}
    str=${str//"!"/"\033[1;37m"}
    str=${str//"@"/"\033[46m "}
    str=${str//"+"/"\033[2;37m"}
    str=${str//">"/"\033[1;30m"}

    str=${str//"█"/"\033[0;37m█"}
    str=${str//"▓"/"\033[47m\033[2;37m▄"}
    str=${str//"▒"/"\033[1;30m█"}
    str=${str//"░"/"\033[2;30m█"}
    str=${str//"▄"/"\033[0m\033[37m▄"}
    str=${str//"▀"/"\033[0m\033[37m▀"}
    str=${str//"$"/"\033[0m\033[30m█"}
    str=${str//"\""/"\033[1;40m\033[37m▀"}
    str=${str//"_"/"\033[1;40m\033[37m▄"}
    str="$str\033[0m"
  fi

  echo $str
}
